#include<unistd.h>

void  ft_swap(int *a, int *b)
{
	int temp = *a;
	*a = *b;
	*b = temp;
}

int main()
{
	int num1 = 6;
	int num2 = 9;
	ft_swap("A=%i,B= %i" num1, num2);
}
